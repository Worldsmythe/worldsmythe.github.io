// Regenerates src/languages.data.ts from GitHub Linguist's languages.yml.
// Run with `bun run update-languages` from the package. Build-time data only —
// it's vendored (committed) so the build never depends on the network.
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { parse } from 'yaml';

const SOURCE =
    'https://raw.githubusercontent.com/github-linguist/linguist/refs/heads/main/lib/linguist/languages.yml';
const OUT = resolve(dirname(fileURLToPath(import.meta.url)), '../src/languages.data.ts');

const res = await fetch(SOURCE);
if (!res.ok) throw new Error(`fetch failed: ${res.status}`);
const langs = parse(await res.text());

// alias/name (lowercased) -> { name, color }; only languages with a color.
const map = {};
const put = (key, value) => {
    const k = String(key).toLowerCase();
    if (!(k in map)) map[k] = value;
};
for (const [name, info] of Object.entries(langs)) {
    if (!info?.color) continue;
    const value = { name, color: info.color };
    put(name, value);
    for (const alias of info.aliases ?? []) put(alias, value);
}

const sorted = Object.fromEntries(Object.entries(map).sort(([a], [b]) => a.localeCompare(b)));
const body = JSON.stringify(sorted, null, '\t');
writeFileSync(
    OUT,
    `// Generated by scripts/update-languages.mjs from GitHub Linguist. Do not edit by hand.\n` +
        `export interface LanguageData {\n\tname: string;\n\tcolor: string;\n}\n\n` +
        `export const LANGUAGES: Record<string, LanguageData> = ${body};\n`,
);
console.log(`wrote ${Object.keys(sorted).length} aliases to ${OUT}`);
